package sort_ALGORITHMS;

public class Insertion_Sort {

	@SuppressWarnings("unused")
	public static void main(String[] args) {
		
		//CLASS CODE
		
		
		//MY CODE
		int[] arr = {1,3,5,8,10,4,2};
		
		int temp;
		
		for (int i = 1; i < arr.length; i++) {
			for (int j = i - 1 ; j >= 0; j++) {
				while (arr[i] < arr[j]) {
					temp = arr[i];
						
				}
			}
			
		}
		
	}

}
